// Helper function to get jobs for a specific organization
export const getJobsByOrganization = (organizationId: string): Job[] => {
  return mockJobs.filter(job => job.organizationId === organizationId);
};

// Helper function to get applications for a specific job
export const getApplicationsByJob = (jobId: string): Application[] => {
  return mockApplications.filter(app => app.jobId === jobId);
};

// Helper function to get applications by status for a specific job
export const getApplicationsByStatus = (jobId: string, status: ApplicationStatus): Application[] => {
  return mockApplications.filter(app => app.jobId === jobId && app.status === status);
};

// Helper function to update application status
export const updateApplicationStatus = (applicationId: string, newStatus: ApplicationStatus): Application | null => {
  const appIndex = mockApplications.findIndex(app => app.id === applicationId);
  if (appIndex === -1) return null;
  
  // In a real app, you would call an API to update the status
  // For this mock, we'll just update the local array
  mockApplications[appIndex] = {
    ...mockApplications[appIndex],
    status: newStatus,
    lastUpdated: new Date().toISOString().split('T')[0],
  };
  
  return mockApplications[appIndex];
};